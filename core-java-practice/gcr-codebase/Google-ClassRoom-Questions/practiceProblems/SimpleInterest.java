package com.bridge.lab;

public class SimpleInterest {

	public static void main(String[] args) {
		// calculate the simple interest 
		// r is rate and p is principle and t is time 
		// formula si = (p*r*t)/100;
		double p = 123.12;
		double r = 12.12;
		double t = 2;
		double si = (p*r*t)/100;
		System.out.println(si);
		
	}

}
